const express = require('express');
var app = express();


//////////////////////////////////////////////
// CONFIG
//////////////////////////////////////////////
app.set("view engine", "hbs");
app.use(express.static('src'));


//////////////////////////////////////////////
// ROUTES
//////////////////////////////////////////////
app.get('/', (req, res) => {
  res.render(__dirname + '/src/index');
});





//////////////////////////////////////////////
// LISTENING
//////////////////////////////////////////////
var port = process.env.PORT || 8000
app.listen(port, () => {
  console.log(`server is up and running on PORT:${port}`);
});
